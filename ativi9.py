x = float(input("Digite um numero"))
z = float(input("Digite um numero"))
div = int(input("Escolha o um numero para 1= soma 2= divisao 3= multiplicacao 4= subtracao"))
if div==1:
    soma=x+z
    print(f"A soma dos dois valores é={soma}")
if div==2:
    divisao=x/z
    print(f"A soma dos dois valores é={divisao}")
if div==3:
    mult=x*z
    print(f"A soma dos dois valores é={mult}")
if div==4:
    sub=x-z
    print(f"A soma dos dois valores é={sub}")